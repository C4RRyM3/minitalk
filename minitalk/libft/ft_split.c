#include "libft.h"

static int	count_words(const char *str, char c)
{
	int	i;
	int	k;

	i = 0;
	k = 0;
	if (str)
	{
		while (*str)
		{
			if (*str != c && k == 0)
			{
				k = 1;
				i++;
			}
			else if (*str == c)
				k = 0;
			str++;
		}
	}
	return (i);
}

char	**ft_free(char **arr)
{
	size_t	i;

	i = 0;
	while (arr[i])
		free(arr[i++]);
	free(arr);
	return (NULL);
}

static char	*word_dup(const char *str, int start, int finish)
{
	char	*word;
	int		i;

	i = 0;
	word = malloc((finish - start + 1) * sizeof(char));
	if (!word)
		return (NULL);
	while (start < finish)
		word[i++] = str[start++];
	word[i] = '\0';
	return (word);
}

char	**ft_split(char const *s, char c)
{
	size_t	i;
	size_t	j;
	int		index;
	char	**split;

	split = malloc((count_words(s, c) + 1) * sizeof(char *));
	if (!s || !(split))
		return (NULL);
	i = -1;
	j = 0;
	index = -1;
	while (++i <= ft_strlen(s))
	{
		if (s[i] != c && index < 0)
			index = i;
		else if ((s[i] == c || i == ft_strlen(s)) && index >= 0)
		{
			split[j++] = word_dup(s, index, i);
			if (!split[j - 1])
				return (ft_free(split));
			index = -1;
		}
	}
	split[j] = 0;
	return (split);
}
