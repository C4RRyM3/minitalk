#include "libft.h"

void	ft_putstr_fd(char const *str, int fd)
{
	size_t	i;

	i = 0;
	if (str)
	{
		while (str[i] != 0)
		{
			write (fd, &str[i], 1);
			i++;
		}
	}
}
